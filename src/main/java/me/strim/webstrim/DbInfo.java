package me.strim.webstrim;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.validation.constraints.Size;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author mihai
 */
@Entity
@Table(name = "db_info")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "DbInfo.findAll", query = "SELECT d FROM DbInfo d")
    , @NamedQuery(name = "DbInfo.findById", query = "SELECT d FROM DbInfo d WHERE d.id = :id")
    , @NamedQuery(name = "DbInfo.findByDatabaseVersion", query = "SELECT d FROM DbInfo d WHERE d.databaseVersion = :databaseVersion")})
public class DbInfo implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Size(max = 45)
    @Column(name = "database_version")
    private String databaseVersion;

    public DbInfo() {
    }

    public DbInfo(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getDatabaseVersion() {
        return databaseVersion;
    }

    public void setDatabaseVersion(String databaseVersion) {
        this.databaseVersion = databaseVersion;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof DbInfo)) {
            return false;
        }
        DbInfo other = (DbInfo) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "me.strim.webstrim.DbInfo[ id=" + id + " ]";
    }
    
}
